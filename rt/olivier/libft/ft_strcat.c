#include "libft.h"

char	*ft_strcat(char *dest, char *src)
{
	int i;
	int len;

	i = 0;
	len = ft_strlen(dest);
	while (src[i] != '\0')
	{
		dest[len] = src[i];
		i++;
		len++;
	}
	dest[len] = '\0';
	return (dest);
}
