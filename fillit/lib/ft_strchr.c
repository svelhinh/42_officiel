#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*s2;
	size_t	i;

	s2 = NULL;
	i = 0;
	while (s[i] != c && i <= ft_strlen(s))
		i++;
	if (s[i] == c)
		s2 = (char*)&s[i];
	return (s2);
}
