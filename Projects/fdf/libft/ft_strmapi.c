#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	size_t	i;
	char	*str;

	i = 0;
	if (!s || !f)
		return (NULL);
	str = (char *)malloc(sizeof(char) * (ft_strlen(s) + 1));
	if (str)
	{
		while (i < ft_strlen(s))
		{
			str[i] = (*f)(i, s[i]);
			i++;
		}
		return (str);
	}
	else
		return (NULL);
}
