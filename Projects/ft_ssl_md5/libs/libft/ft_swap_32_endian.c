#include "libft.h"

unsigned int	ft_swap_32_endian(unsigned int nb)
{
	unsigned int y;

	y = 0;
	y += (nb & 0x000000FF) << 24;
	y += (nb & 0xFF000000) >> 24;
	y += (nb & 0x0000FF00) << 8;
	y += (nb & 0x00FF0000) >> 8;
	return (y);
}
