#include "wolf3d.h"

void		ft_swap(float *a, float *b)
{
	float c;

	c = *a;
	*a = *b;
	*b = c;
}

void		init_var(t_ray *r)
{
	r->posx = 18.5;//8.2;
	r->posy = 8.5;//1.5;
	r->dirx = -1;
	r->diry = 0;
	r->planex = 0;
	r->planey = 0.66;
}
