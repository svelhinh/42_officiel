#include <string.h>

void		*ft_memset(void *s, int c, size_t len)
{
	size_t	i;
	char	*str;

	i = 0;
	str = (char*)s;
	while (i < len)
	{
		str[i] = c;
		i++;
	}
	return (s);
}
